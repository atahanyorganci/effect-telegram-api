import { renderSchemaExpr, type RenderRefStrategy } from "./render-type-expr.ts";
import type { Method, Parameter } from "../parse/model.ts";

const GENERATED_HEADER = "// Generated by `pnpm codegen` from data/spec. Do not edit manually.";

const methodStrategy: RenderRefStrategy = {
	schemaRef: name => `Objects.${name}`,
	fileSchema: () => "Objects.InputFile",
};

const jsDoc = (description: string): string => {
	const text = description.trim().replace(/\*\//g, "*\\/");
	return text.length === 0 ? "" : `/** ${text} */\n`;
};

const renderPayloadField = (parameter: Parameter): string => {
	const expr = renderSchemaExpr(parameter.type, methodStrategy);
	return parameter.required ? `\t\t${parameter.name}: ${expr},` : `\t\t${parameter.name}: Schema.optional(${expr}),`;
};

const renderMethod = (method: Method): string => {
	const doc = jsDoc(method.description);
	const success = `\tsuccess: ${renderSchemaExpr(method.returns, methodStrategy)},`;

	if (method.parameters.length === 0) {
		return `${doc}export const ${method.name} = Rpc.make("${method.name}", {\n${success}\n});`;
	}

	const payload = `\tpayload: {\n${method.parameters.map(renderPayloadField).join("\n")}\n\t},`;
	return `${doc}export const ${method.name} = Rpc.make("${method.name}", {\n${payload}\n${success}\n});`;
};

export const renderMethodsModule = (methods: readonly Method[]): string => {
	const sorted = [...methods].sort((a, b) => a.name.localeCompare(b.name));
	const group = `export const TelegramBotApi = RpcGroup.make(\n${sorted
		.map(method => `\t${method.name},`)
		.join("\n")}\n);`;

	const sections = [
		GENERATED_HEADER,
		[
			`import * as Rpc from "effect/unstable/rpc/Rpc";`,
			`import * as RpcGroup from "effect/unstable/rpc/RpcGroup";`,
			`import * as Schema from "effect/Schema";`,
			`import * as Objects from "./Objects.ts";`,
		].join("\n"),
		...sorted.map(renderMethod),
		group,
	];

	return `${sections.join("\n\n")}\n`;
};
